function question(query, defaultVal = '') {
  return new Promise(resolve => {
    const prompt = defaultVal ? `${query} [${defaultVal}]: ` : `${query}: `;
    rl.question(prompt, (answer) => {
      resolve(answer.trim() || defaultVal);
    });
  });
}

